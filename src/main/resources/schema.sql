DROP TABLE IF EXISTS users, requests, items, bookings, comments CASCADE;

CREATE TABLE IF NOT EXISTS users (
user_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
user_name VARCHAR(70) NOT NULL,
email VARCHAR(70) NOT NULL,
CONSTRAINT UQ_USER_EMAIL UNIQUE (email)
);

CREATE TABLE IF NOT EXISTS requests(
request_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
description VARCHAR(250),
requester_id BIGINT NOT NULL,
created TIMESTAMP WITHOUT TIME ZONE NOT NULL,
FOREIGN KEY (requester_id) REFERENCES users(user_id)
);

CREATE TABLE IF NOT EXISTS items (
item_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
item_name VARCHAR(70) NOT NULL,
description VARCHAR(250) NOT NULL,
is_available BOOLEAN NOT NULL,
owner_id BIGINT NOT NULL,
request_id BIGINT,
FOREIGN KEY (owner_id) REFERENCES users(user_id),
FOREIGN KEY (request_id) REFERENCES requests(request_id)
);

CREATE TABLE IF NOT EXISTS bookings (
booking_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
booking_start TIMESTAMP WITHOUT TIME ZONE NOT NULL,
booking_end TIMESTAMP WITHOUT TIME ZONE NOT NULL,
booker_id BIGINT NOT NULL,
item_id BIGINT NOT NULL,
status VARCHAR NOT NULL,
FOREIGN KEY (booker_id)  REFERENCES users(user_id),
FOREIGN KEY (item_id)  REFERENCES items(item_id),
CONSTRAINT constrain_status CHECK (status IN ('WAITING', 'APPROVED', 'REJECTED', 'CANCELED'))
);

CREATE TABLE IF NOT EXISTS comments(
comment_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
text VARCHAR(250) NOT NULL,
item_id BIGINT NOT NULL,
author_id BIGINT NOT NULL,
comment_created TIMESTAMP WITHOUT TIME ZONE NOT NULL,
FOREIGN KEY (author_id)  REFERENCES users(user_id),
FOREIGN KEY (item_id)  REFERENCES items(item_id)
);